<h1>OUR PROCESS</h1>
<div class="column left">

<h2>Design Programming</h2>
<p>Before design work can begin in earnest, a written program for the project is developed. This program describes, in as much detail as possible, the client's architectural and functional objectives, as well as the overall project scope.</p>
<p>Some of our clients come to us having already developed a design program, while others merely have general objectives in mind. Our staff can work with clients to fully develop their written program in either case.</p>

<h2>Code/Zoning Research</h2>
<p>Before the design for any project can be developed, the rules governing the development must be thoroughly researched, on a site-specific basis, and understood. In recent years these regulations have become increasingly more complex, and can vary dramatically from one jurisdiction to the next.</p>

<h2>Documentation of Existing Conditions</h2>
<p>Whether remodeling or building a new home, the existing conditions at the project site must be carefully documented in order to approach the design process intelligently.</p>
<p>In the case of a new home, this documentation consists of a topography map and property line survey, showing the physical characteristics, recorded property lines and any easements. This work is contracted to a qualified surveyor or civil engineering consultant.</p>
<p>In the case of a remodel, "as-built" plans, based on carefully collected measurements, must be drafted before design work can begin. This work is done by the staff of <em>Flury Bryant Design Group</em>.</p>

<h2>Schematic Design</h2>
<p>Once the design program has been established and the regulations governing the development of the project are established, the design process begins. During the Schematic Design Phase, broad-brush planning and design directions are explored in a very preliminary fashion. Potential solutions are presented for further discussion and critique by the client.</p>

</div>
<div class="column right">

<h2>Design Development</h2>
<p>Once a schematic direction has been determined, the Design Development Phase is initiated. During this phase, the design is further developed through the generation of more formal drawings. Typically, these drawings will consist of site, floor, and roof plans, and exterior elevations indicating material selections and building masses.</p>
<p>Aside from providing a vehicle to further study and understand the design, these drawings are typically used by prospective builders to develop a preliminary construction budget for the project, and eventually become part of the drawing package necessary to obtain any required preliminary design approval from the governing municipality.</p>
<p>Depending largely on the scope of a project, the approval process in many jurisdictions may include a formal design review hearing, often consisting of a public forum where neighborhood input is solicited. The specific submittal requirements vary from one municipality to another, and may include participation by other members of the design team, such as a civil or geotechnical engineer, and a landscape architect.</p>

<h2>Construction Documents</h2>
<p>The construction documents for any project contain all of the details required for a building permit application and, ultimately, for construction. Depending on the scope of the project, a team of consultants is assembled and orchestrated by our staff to prepare supporting documents for those drawings prepared by <em>Flury Bryant Design Group</em>, such as soils reports, structural calculations, energy calculations, etc.</p>

<h2>Plan-Check Phase</h2>
<p>Once a completed building permit application has been submitted with all of the required documentation, the Planning and Building Departments of the municipality having jurisdiction will review the documents for building code and zoning ordinance compliance, as well as general content and coordination across the various disciplines. This review will often involve other departments and agencies such as the Fire Department, Public Works, the Engineering Department, the local school district, etc. This typically results in a list of comments that must be addressed by the design team before a building permit can be issued.</p>
<p>Once these comments have been satisfactorily addressed, the building permit is issued, and construction can begin.</p>

<h2>Construction Administration</h2>
<p>During the construction phase of the project, <em>Flury Bryant Design Group</em> staff is available to provide support services at whatever level is appropriate. Generally, larger projects will require more support than smaller ones. However, since each project has its own unique set of circumstances, this is evaluated on a project by project basis.</p>

